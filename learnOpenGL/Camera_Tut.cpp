	glm::vec3 up = glm::vec3(0.0f, 1.0f, 0.0f);	//points up in WORLD space
	glm::vec3 cameraRight = glm::normalize(glm::cross(up, cameraDirection));

	glm::vec3 cameraUp = glm::cross(cameraDirection, cameraRight); //points up in camera space*/


